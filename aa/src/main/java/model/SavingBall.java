package model;

public class SavingBall {
    private double x;
    private double y;
    private String color;

    public SavingBall(double x, double y, String color) {
        this.y = y;
        this.x = x;
        this.color = color;
    }

    public double getY() {
        return y;
    }

    public double getX() {
        return x;
    }
}
